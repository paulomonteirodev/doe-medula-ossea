                <h1>DOENÇAS RELACIONADAS À MEDULA ÓSSEA</h1>
                <p>Doenças da medula óssea podem levar a uma anomalia na produção de qualquer as células maduras do sangue ou suas células imaturas precursoras ou antecessor. Os principais tipos de problemas com a medula óssea incluem:</p>
                <ul class="lipontos">
                    <li>Aumento da produção de um tipo de célula - estes pacotes até a medula com um tipo de células e diminui a produção de outros tipos de células;</li>
                    <li>Aumento na linha de uma célula, porque as células não morrem em tempos normais;</li>
                    <li>Produção de células imaturas que não amadurecem ou função corretamente;</li>
                    <li>Produção de células frágeis que facilmente morrem ou produzir o menor número de células;</li>
                    <li>Aumento da rede de tecido fibroso apoio levando a formação de células anormais e diminuídos números de células;</li>
                    <li>Falta de ferro, dificultando a formação de RBC;</li>
                    <li>Propagação de outras doenças de medula óssea.</li>
                </ul>
                <p>As doenças e distúrbios da medula óssea incluem leucemia, síndrome mielodisplásica, desordens mieloproliferativas, anemia aplástica, e entre outros distúrbios.</p>
