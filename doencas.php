                <p>
                    <span>Síndromes mielodisplásicas:</span> é um grupo de doenças em que há anormalidades na produção de células da medula óssea, podendo causar anemia, infecções e sangramentos anormais. Síndromes são classificadas por células em esfregaço de medula e sangue o osso aparência ao microscópio. É frequente a evolução de síndromes mielodisplásicas para leucemia mieloide aguda.
                </p><br />

                <p>
                    <span>Doenças mieloproliferativas:</span> são distúrbios em que há proliferação excessiva de uma ou mais linhagens de células do sangue ou da matriz fibrosa da medula óssea. O acúmulo de uma linhagem de células pode prejudicar ou deslocar o desenvolvimento das outras. A medula óssea em MPD mostra uma mistura de células em vários estágios de maturidade.<br />
                    A anemia aplástica é causada por falta de células tronco precursoras ou por modificação do ambiente da medula óssea. Os danos podem resultar da exposição a substâncias químicas como o benzeno, radiação ou certos medicamentos.
                </p><br />

                <p>
                    <span>Anemia aplástica:</span> esta é uma condição onde há perda ou supressão da produção de glóbulos vermelhos. Isto pode ser devido a um defeito na célula tronco produzi-los ou devido a uma lesão no ambiente da medula óssea.
                    Os danos podem resultar da exposição a substâncias químicas como o benzeno, radiação ou certos medicamentos ou pode ser genético, por exemplo, Fanconi ' anemia s ou associada a uma infecção viral com vírus parvo.
                </p><br />

                <p>
                    <span>Anemias de deficiência de ferro:</span> anemias de deficiência de ferro levam à formação de hemácias deformadas e menores liberado da medula. Estas são pequenas e pálidas e são chamadas hemácias microcítica.
                    Anemias também podem ser causadas por deficiência ou disfunção de eritropoietina, um produto químico produzido pelos rins que estimula a produção de RBC.
                </p><br />

                <h2>Outras doenças e distúrbios da medula óssea:</h2>
                <ul class="lipontos">
                    <li>Distúrbios dos plasmócitos, associados a expansão de um clone de linfócitos B e produção de seu anticorpo correspondente;</li>
                    <li>Linfomas e outros tumores que podem invadir a medula óssea e prejudicar suas funções;</li>
                    <li>Anemias causadas por deficiências (como a de ferro) por defeitos hereditários da membrana celular ou da hemoglobina;</li>
                    <li>Anemias causadas por deficiência da produção de eritropoietina pelos rins;</li>
                    <li>Púrpura trombocitopênica trombótica-isso resulta na redução da produção de plaquetas e sangrando tendências;</li>
                    <li>Citopenia inexplicável – isso resulta em diminuição na produção de todos os tipos de células;</li>
                    <li>Outras causas incluem os tumores de células pequenas da infância, doença de mastócitos, doença granulomatosa disseminada, amiloidose primária, doença óssea metabólica etc;</li>
                    <li>Depressão da medula óssea pode ser causada devido à quimioterapia, transplante de medula óssea e radioterapia do câncer.</li>
                </ul><br />

                <span style="font-size: 20px;">Sinais e sintomas</span>
                <p>Os distúrbios da medula óssea podem ser graves, com risco de vida. Sintomas agudos estão relacionados a excesso ou falta de células do sangue. Os mais comuns são:</p>
                <ul class="lipontos">
                    <li>Febre;</li>
                    <li>Fraqueza, fadiga e palidez provocadas por anemia;</li>
                    <li>Perda de peso;</li>
                    <li>Aumento de linfonodos, do fígado ou do baço;</li>
                    <li>Sangramentos anormais;</li>
                    <li>Suores noturnos;</li>
                    <li>Dores ósseas e articulares;</li>
                    <li>Infecções frequentes;</li>
                    <li>Cefaleia (dor de cabeça), vômitos, alteração da consciência e convulsões (envolvimento do sistema nervoso central).</li>
                </ul>
