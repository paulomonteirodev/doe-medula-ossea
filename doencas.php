                <p><span>Leucemia:</span> é um câncer das células brancas do sangue que podem afetar qualquer um dos cinco tipos de WBC, ou seja, é uma proliferação de leucócitos com acúmulo de células imaturas ou maduras. Pacientes com leucemia podem ter infecções frequentes, anemia, sangramento, hematomas, suores noturnos e dor óssea e articulação. O baço, o fígado e os linfonodos podem aumentar por acúmulo de células leucêmicas.</p>
